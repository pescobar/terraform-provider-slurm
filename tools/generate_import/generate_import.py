# QOS generation
# ---------------------------------------------------------------------------

def _qos_attrs(q: dict) -> list:
    """Build the (key, value) attribute list for a slurm_qos resource block."""
    attrs = [("name", _q(q["name"]))]

    if q.get("description"):
        attrs.append(("description", _q(q["description"])))

    prio = sint(q.get("priority"))
    if prio:
        attrs.append(("priority", str(prio)))

    lim  = q.get("limits") or {}
    grace = lim.get("grace_time")
    if isinstance(grace, int) and grace > 0:
        attrs.append(("grace_time", str(grace)))

    mx = (lim.get("max") or {})

    # Wall-clock limits
    # limits.max.wall_clock.per.job  → max_wall_pj
    # limits.max.wall_clock.per.qos  → grp_wall
    wc_per = ((mx.get("wall_clock") or {}).get("per") or {})
    if v := sint(wc_per.get("job")):
        attrs.append(("max_wall_pj", str(v)))
    if v := sint(wc_per.get("qos")):
        attrs.append(("grp_wall", str(v)))

    # Job-count limits
    # limits.max.jobs.count                    → grp_submit_jobs
    # limits.max.jobs.per.user                 → max_jobs_per_user
    # limits.max.jobs.per.account              → max_jobs_per_account
    # limits.max.jobs.active_jobs.per.user     → max_submit_jobs_per_user
    # limits.max.jobs.active_jobs.per.account  → max_submit_jobs_per_account
    # limits.max.active_jobs.count             → grp_jobs
    jobs   = mx.get("jobs") or {}
    if v := sint(jobs.get("count")):
        attrs.append(("grp_submit_jobs", str(v)))
    j_per  = jobs.get("per") or {}
    if v := sint(j_per.get("user")):
        attrs.append(("max_jobs_per_user", str(v)))
    if v := sint(j_per.get("account")):
        attrs.append(("max_jobs_per_account", str(v)))
    aj_per = (jobs.get("active_jobs") or {}).get("per") or {}
    if v := sint(aj_per.get("user")):
        attrs.append(("max_submit_jobs_per_user", str(v)))
    if v := sint(aj_per.get("account")):
        attrs.append(("max_submit_jobs_per_account", str(v)))
    if v := sint((mx.get("active_jobs") or {}).get("count")):
        attrs.append(("grp_jobs", str(v)))

    # TRES limits
    # limits.max.tres.total              → grp_tres
    # limits.max.tres.minutes.total      → grp_tres_mins
    # limits.max.tres.per.job            → max_tres_per_job
    # limits.max.tres.per.node           → max_tres_per_node
    # limits.max.tres.per.user           → max_tres_per_user
    # limits.max.tres.per.account        → max_tres_per_account
    # limits.max.tres.minutes.per.job    → max_tres_mins_per_job
    # limits.max.tres.minutes.per.user   → max_tres_mins_per_user
    # limits.max.tres.minutes.per.account→ max_tres_mins_per_account
    # limits.min.tres.per.job            → min_tres_per_job
    tres      = mx.get("tres") or {}
    tres_per  = tres.get("per") or {}
    tres_mins = tres.get("minutes") or {}
    mins_per  = tres_mins.get("per") or {}

    for attr, raw in (
        ("grp_tres",               tres.get("total")),
        ("grp_tres_mins",          tres_mins.get("total")),
        ("max_tres_per_job",       tres_per.get("job")),
        ("max_tres_per_node",      tres_per.get("node")),
        ("max_tres_per_user",      tres_per.get("user")),
        ("max_tres_per_account",   tres_per.get("account")),
        ("max_tres_mins_per_job",  mins_per.get("job")),
        ("max_tres_mins_per_user", mins_per.get("user")),
        ("max_tres_mins_per_account", mins_per.get("account")),
    ):
        if entries := _norm_tres(raw):
            attrs.append((attr, _tres_list(entries)))

    mn_tres_per = ((lim.get("min") or {}).get("tres") or {}).get("per") or {}
    if entries := _norm_tres(mn_tres_per.get("job")):
        attrs.append(("min_tres_per_job", _tres_list(entries)))

    # Flags — skip empty / "NONE" sentinels
    flags = [f for f in (q.get("flags") or []) if f.upper() not in ("NONE", "")]
    if flags:
        attrs.append(("flags", _strlist(flags)))

    # Preemption
    preempt = q.get("preempt") or {}
    if plist := [x for x in (preempt.get("list") or []) if x]:
        attrs.append(("preempt_list", _strlist(plist)))
    if pmodes := [m.upper() for m in (preempt.get("mode") or [])
                  if m.upper() not in ("OFF", "NONE", "DISABLED", "")]:
        attrs.append(("preempt_mode", _strlist(pmodes)))
    if v := sint(preempt.get("exempt_time")):
        attrs.append(("preempt_exempt_time", str(v)))

    # Usage accounting
    uf = sfloat(q.get("usage_factor"))
    if uf is not None and abs(uf - 1.0) > 1e-9:
        attrs.append(("usage_factor", str(uf)))
    ut = sfloat(q.get("usage_threshold"))
    if ut is not None and ut != 0.0:
        attrs.append(("usage_threshold", str(ut)))

    return attrs


def generate_qos(qos_list: list) -> tuple:
    """Returns (hcl_content, import_blocks, warnings)."""
    warnings = []
    blocks   = []
    imports  = []

    for q in sorted(qos_list, key=lambda x: x["name"]):
        name  = q["name"]
        label = tf_label(name, "qos")
        is_system = name in SYSTEM_QOS

        if is_system:
            warnings.append(
                f"  QOS '{name}' is a built-in Slurm QOS (auto-created at DB init).\n"
                f"  It is included in the generated config but must NEVER be destroyed.\n"
                f"  To stop managing it: tofu state rm slurm_qos.{label}"
            )
            comment = (
                f"# WARNING: '{name}' is a built-in Slurm QOS created automatically at\n"
                f"# database initialisation. You can configure it via OpenTofu but you\n"
                f"# MUST NOT run 'tofu destroy' on this resource — deleting it via the\n"
                f"# REST API corrupts slurmdbd. To stop managing it without deleting it:\n"
                f"#   tofu state rm slurm_qos.{label}\n"
            )
        else:
            comment = ""

        blocks.append(resource_block("slurm_qos", label, _qos_attrs(q), comment))
        imports.append(import_block("slurm_qos", label, name))

    return "\n\n".join(blocks), imports, warnings


