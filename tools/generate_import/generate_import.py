# User / association generation
# ---------------------------------------------------------------------------

def _assoc_fields(a: dict, acct: Optional[dict] = None) -> list:
    """
    Extract a user↔account association's attributes as native (key, value)
    pairs, EXCLUDING the account itself. Single source of truth shared by the
    flat (HCL association block) and big-cluster (YAML member override) emitters.

    `acct` is the parent account-level association (user=""), used to detect
    values the user association is merely INHERITING rather than explicitly
    overriding. Empirically verified against a live cluster: Slurm's REST API
    resolves default_qos, max_jobs, max_tres_per_job, max_tres_per_node, and
    max_tres_mins_per_job to the account's own effective value even when the
    user's association never set them — there is no separate flag marking a
    value as inherited vs. explicit. A naive "emit whatever is non-zero"
    reading would therefore convert implicit inheritance into a permanent
    per-user pin, silently breaking future account-limit changes from
    propagating to that user. grp_tres / grp_tres_mins / grp_tres_run_mins
    do NOT inherit this way (also verified) and are always the association's
    own explicit value, so they are emitted unconditionally.
    """
    acct = acct or {}
    fields: list = []

    if partition := (a.get("partition") or ""):
        fields.append(("partition", partition))

    if (fs := _fairshare_emit(a.get("shares_raw"))) is not None:
        fields.append(("fairshare", fs))

    # default_qos — see inheritance note in the docstring.
    dflt_qos      = (a.get("default") or {}).get("qos") or ""
    acct_dflt_qos = (acct.get("default") or {}).get("qos") or ""
    if dflt_qos and dflt_qos != acct_dflt_qos:
        fields.append(("default_qos", dflt_qos))

    mx      = a.get("max") or {}
    jobs    = mx.get("jobs") or {}
    per     = jobs.get("per") or {}
    acct_mx = acct.get("max") or {}

    # max_jobs — see inheritance note in the docstring.
    # max.jobs.active → max_jobs
    acct_max_jobs = sint((acct_mx.get("jobs") or {}).get("active"))
    if v := sint(jobs.get("active")):
        if v != acct_max_jobs:
            fields.append(("max_jobs", v))

    # The remaining job-count / wall-clock limits below have no slurm_account
    # equivalent to inherit from in this provider's schema, so whatever Slurm
    # returns here is always the association's own explicit value.
    # max.jobs.accruing       → max_jobs_accrue
    # max.jobs.total          → max_submit_jobs
    # max.jobs.per.count      → grp_jobs
    # max.jobs.per.accruing   → grp_jobs_accrue
    # max.jobs.per.submitted  → grp_submit_jobs
    # max.jobs.per.wall_clock → max_wall_pj  (minutes)
    for attr, obj in (
        ("max_jobs_accrue", jobs.get("accruing")),
        ("max_submit_jobs", jobs.get("total")),
        ("grp_jobs",        per.get("count")),
        ("grp_jobs_accrue", per.get("accruing")),
        ("grp_submit_jobs", per.get("submitted")),
        ("max_wall_pj",     per.get("wall_clock")),
    ):
        if v := sint(obj):
            fields.append((attr, v))

    # max.per.account.wall_clock → grp_wall (minutes)
    if v := sint(((mx.get("per") or {}).get("account") or {}).get("wall_clock")):
        fields.append(("grp_wall", v))

    if v := sint(a.get("priority")):
        fields.append(("priority", v))

    # TRES limits
    # max.tres.per.job         → max_tres_per_job
    # max.tres.per.node        → max_tres_per_node
    # max.tres.minutes.per.job → max_tres_mins_per_job
    # max.tres.total           → grp_tres
    # max.tres.group.minutes   → grp_tres_mins
    # max.tres.group.active    → grp_tres_run_mins
    tres      = mx.get("tres") or {}
    tres_per  = tres.get("per") or {}
    tres_mins = tres.get("minutes") or {}
    tres_grp  = tres.get("group") or {}

    acct_tres      = acct_mx.get("tres") or {}
    acct_tres_per  = acct_tres.get("per") or {}
    acct_tres_mins = acct_tres.get("minutes") or {}

    # max_tres_per_job / max_tres_per_node / max_tres_mins_per_job — see
    # inheritance note in the docstring.
    for attr, raw, acct_raw in (
        ("max_tres_per_job", tres_per.get("job"), acct_tres_per.get("job")),
        ("max_tres_per_node", tres_per.get("node"), acct_tres_per.get("node")),
        ("max_tres_mins_per_job",
         (tres_mins.get("per") or {}).get("job"),
         (acct_tres_mins.get("per") or {}).get("job")),
    ):
        entries = _norm_tres(raw)
        if entries and entries != _norm_tres(acct_raw):
            fields.append((attr, entries))

    # grp_tres / grp_tres_mins / grp_tres_run_mins do not inherit — always
    # the association's own explicit value (see docstring).
    for attr, raw in (
        ("grp_tres",          tres.get("total")),
        ("grp_tres_mins",     tres_grp.get("minutes")),
        ("grp_tres_run_mins", tres_grp.get("active")),
    ):
        if entries := _norm_tres(raw):
            fields.append((attr, entries))

    # allowed_qos — only emit when explicitly set on this association and differs
    # from the parent account's effective list.  An empty list or a list that
    # matches the account's list means the user is just inheriting.
    # (a.get("qos") reads the raw Slurm API field, which is still named "qos" on
    # the wire; the output key is the provider's "allowed_qos" attribute name.)
    qos_raw  = [x for x in (a.get("qos") or []) if x]
    acct_qos = sorted(x for x in (acct.get("qos") or []) if x)
    if qos_raw and sorted(qos_raw) != acct_qos:
        fields.append(("allowed_qos", qos_raw))

    return fields


def _assoc_block(a: dict, acct: Optional[dict] = None) -> str:
    """Render a single association {} nested block for a slurm_user resource."""
    lines = ["association {", f"  account = {_q(a['account'])}"]
    for key, val in _assoc_fields(a, acct):
        lines.append(f"  {key} = {_hcl_val(val)}")
    lines.append("}")
    return "\n".join(lines)


def generate_users(users: list, assocs: list) -> tuple:
    """Returns (hcl_content, import_blocks, skipped)."""
    # Account-level association lookup used to filter inherited QOS lists.
    acct_assoc: dict = {
        a["account"]: a
        for a in assocs
        if (a.get("user") == "" or a.get("user") is None) and a.get("account")
    }

    # Index user-level associations by username.
    user_assocs: dict = defaultdict(list)
    for a in assocs:
        user = a.get("user") or ""
        if user and user not in SYSTEM_USERS:
            user_assocs[user].append(a)

    blocks   = []
    imports  = []
    skipped  = []

    for u in sorted(users, key=lambda x: x["name"]):
        name = u["name"]
        if name in SYSTEM_USERS:
            continue

        # A slurm_user resource requires default_account and at least one
        # association block -- both are impossible to populate for a user
        # with no associations at all (e.g. one left over from an
        # out-of-band failure partway through creation). Skip and warn
        # rather than emit HCL that fails validation, matching the
        # zero-association handling already in generate_bigcluster_users().
        if not user_assocs[name]:
            skipped.append(name)
            continue

        label = tf_label(name, "user")

        attrs: list = [("name", _q(name))]

        # administrator_level is a list; take the first element.
        admin_levels = u.get("administrator_level") or []
        admin = admin_levels[0] if admin_levels else ""
        if admin and admin.lower() not in ("none", ""):
            attrs.append(("admin_level", _q(admin)))

        # The Slurm REST API always returns default.account="" on the user
        # object (same bug the provider works around in its Read function).
        # Derive default_account from the association that has is_default=true.
        default_acct = next(
            (a.get("account", "") for a in user_assocs[name] if a.get("is_default")),
            "",
        )
        if default_acct:
            attrs.append(("default_account", _q(default_acct)))

        wckey = (u.get("default") or {}).get("wckey") or ""
        if wckey:
            attrs.append(("default_wc_key", _q(wckey)))

        # One association block per user+account combination, sorted for
        # deterministic output.
        for a in sorted(user_assocs[name], key=lambda x: x.get("account", "")):
            account_name = a.get("account") or ""
            parent_assoc = acct_assoc.get(account_name) or {}
            attrs.append((_assoc_block(a, parent_assoc), None))

        blocks.append(resource_block("slurm_user", label, attrs))
        imports.append(import_block("slurm_user", label, name))

    return "\n\n".join(blocks), imports, skipped


# ---------------------------------------------------------------------------
# Big-cluster layout  (account-centric YAML data + for_each generation)
#
# Emits a self-contained directory:
#   main.tf              provider config
#   qos.tf               QOS as plain HCL (same as flat)
#   generate.tf          locals + for_each that invert data/ into resources
#   data/accounts/*.yaml       one file per account: metadata + member list
#   data/users/admin_level.yaml      exceptions: non-default admin_level
#   data/users/default_accounts.yaml exceptions: multi-account default pins
#   data/users/wckeys.yaml           exceptions: pinned default_wc_key
#   imports.tf           import blocks targeting the for_each addresses
# ---------------------------------------------------------------------------

def import_block_indexed(rtype: str, base: str, key: str, slurm_id: str) -> str:
    """Import block targeting a for_each instance, e.g. slurm_account.this["x"]."""
    return f'import {{\n  to = {rtype}.{base}[{_q(key)}]\n  id = {_q(slurm_id)}\n}}'


def _safe_stem(name: str, used: dict) -> str:
    """
    Map a Slurm account name to a safe, unique YAML filename stem. The real name
    is preserved in the file's `name:` key, so the stem only needs to be a valid,
    collision-free filename (handles names with '/', spaces, etc.).
    """
    stem = re.sub(r"[^A-Za-z0-9._-]", "_", name)
    if not stem or stem in (".", ".."):
        stem = "account"
    base, n = stem, 1
    while stem in used and used[stem] != name:
        n += 1
        stem = f"{base}_{n}"
    used[stem] = name
    return stem


def _yaml_scalar(v) -> str:
    """Render a scalar for YAML. Strings are always double-quoted (robust)."""
    if isinstance(v, bool):
        return "true" if v else "false"
    if isinstance(v, int):
        return str(v)
    if isinstance(v, float):
        return repr(v)
    return '"' + str(v).replace("\\", "\\\\").replace('"', '\\"') + '"'


def _yaml_kv(key: str, val, indent: int = 0) -> str:
    """Render a `key: value` YAML line (or block for lists), matching our types."""
    pad = " " * indent
    if isinstance(val, list):
        if val and isinstance(val[0], dict):  # TRES list -> block of flow maps
            out = [f"{pad}{key}:"]
            for d in val:
                inner = ", ".join(f"{k}: {_yaml_scalar(vv)}" for k, vv in d.items())
                out.append(f"{pad}  - {{ {inner} }}")
            return "\n".join(out)
        inline = ", ".join(_yaml_scalar(x) for x in val)
        return f"{pad}{key}: [{inline}]"
    return f"{pad}{key}: {_yaml_scalar(val)}"


# Association fields that also exist on slurm_account. A value here
# OVERRIDES what the member would otherwise inherit from the account, so
# they render under the `account_overrides:` sub-key in the big-cluster
# YAML layout.
ACCOUNT_OVERRIDE_FIELDS = frozenset((
    "fairshare", "default_qos", "allowed_qos", "max_jobs",
    "max_tres_per_job", "max_tres_per_node", "max_tres_mins_per_job",
    "grp_tres", "grp_tres_mins", "grp_tres_run_mins",
))
# Everything else _assoc_fields() can return (partition, priority,
# max_jobs_accrue, max_submit_jobs, max_wall_pj, grp_jobs, grp_jobs_accrue,
# grp_submit_jobs, grp_wall) has no slurm_account equivalent at all -- those
# render under `association:` instead, since there is nothing to "override",
# only to declare for this specific user+account pairing.


def _render_account_yaml(fields: list, user_associations: list) -> str:
    """
    fields: [(key, native_value)] incl. name.
    user_associations: [(user, assoc_fields)] where assoc_fields is the flat
    (key, value) list _assoc_fields() returns, split here into the
    account_overrides / association sub-keys documented in README.md.
    """
    lines = ["# Generated by tools/generate_import/generate_import.py",
             "# Edit `user_associations` freely; re-running the tool overwrites this file."]
    for key, val in fields:
        lines.append(_yaml_kv(key, val))
    if user_associations:
        lines.append("user_associations:")
        for user, ofields in user_associations:
            if not ofields:
                lines.append(f"  - {_yaml_scalar(user)}")
                continue
            lines.append(f"  - user: {_yaml_scalar(user)}")
            overrides = [(k, v) for k, v in ofields if k in ACCOUNT_OVERRIDE_FIELDS]
            association = [(k, v) for k, v in ofields if k not in ACCOUNT_OVERRIDE_FIELDS]
            if overrides:
                lines.append("    account_overrides:")
                for k, v in overrides:
                    lines.append(_yaml_kv(k, v, indent=6))
            if association:
                lines.append("    association:")
                for k, v in association:
                    lines.append(_yaml_kv(k, v, indent=6))
    else:
        lines.append("user_associations: []")
    return "\n".join(lines) + "\n"


def _render_user_exception_file(header: list, exceptions: dict, key: str) -> str:
    """Render one flat `user: value` YAML file for a single exception key
    (admin_level, default_wc_key, or default_account) -- one concern per
    file, split out so each stays scannable as the cluster grows (see
    "data/users/ -- exceptions only" in examples/big-cluster/README.md)."""
    lines = list(header) + [""]
    entries = {u: info[key] for u, info in exceptions.items() if key in info}
    if not entries:
        lines.append("{}")
        return "\n".join(lines) + "\n"
    for user in sorted(entries):
        lines.append(f"{_yaml_scalar(user)}: {_yaml_scalar(entries[user])}")
    return "\n".join(lines) + "\n"


