def _account_fields(acc: dict, assoc: Optional[dict], root_qos: list) -> list:
    """
    Extract account attributes as native (key, value) pairs — the single source
    of truth shared by the flat (HCL) and big-cluster (YAML) emitters. Values
    are native ints / strs / lists (of str, or of TRES dicts). Excludes
    layout-specific bits (flat depends_on, big-cluster user_associations).
    """
    fields: list = [("name", acc["name"])]

    if desc := (acc.get("description") or ""):
        fields.append(("description", desc))
    # organization defaults to the account name in Slurm when not explicitly
    # set (verified against a live cluster: sacctmgr shows Organization ==
    # Account for an account created without one) -- skip it in that case,
    # same "indistinguishable from Slurm's own default" rule already applied
    # to fairshare below.
    org = acc.get("organization") or ""
    if org and org != acc["name"]:
        fields.append(("organization", org))

    # parent_account is NOT returned by the /accounts/ endpoint; read it from
    # the account-level association (user="") which always carries it.
    parent = (assoc or {}).get("parent_account") or ""
    if parent and parent not in SYSTEM_ACCOUNTS:
        fields.append(("parent_account", parent))

    if assoc:
        # fairshare emits "parent" for the INT32_MAX sentinel, a decimal string
        # for a real weight, or nothing for Slurm's own defaults -- see
        # _fairshare_emit().
        if (fs := _fairshare_emit(assoc.get("shares_raw"))) is not None:
            fields.append(("fairshare", fs))

        if dflt_qos := (assoc.get("default") or {}).get("qos") or "":
            fields.append(("default_qos", dflt_qos))

        qos_list = sorted(x for x in (assoc.get("qos") or []) if x)
        # Skip allowed_qos when it matches the root association's default qos
        # list — those accounts have no explicit qos set and are just inheriting.
        if qos_list and qos_list != root_qos:
            fields.append(("allowed_qos", qos_list))

        if v := sint((assoc.get("max") or {}).get("jobs", {}).get("active")):
            fields.append(("max_jobs", v))

        # TRES limits on the account-level association
        # max.tres.per.job         → max_tres_per_job
        # max.tres.per.node        → max_tres_per_node
        # max.tres.minutes.per.job → max_tres_mins_per_job
        # max.tres.total           → grp_tres
        # max.tres.group.minutes   → grp_tres_mins
        # max.tres.group.active    → grp_tres_run_mins
        mx        = assoc.get("max") or {}
        tres      = mx.get("tres") or {}
        tres_per  = tres.get("per") or {}
        tres_mins = tres.get("minutes") or {}
        tres_grp  = tres.get("group") or {}

        for attr, raw in (
            ("max_tres_per_job",    tres_per.get("job")),
            ("max_tres_per_node",   tres_per.get("node")),
            ("max_tres_mins_per_job", (tres_mins.get("per") or {}).get("job")),
            ("grp_tres",            tres.get("total")),
            ("grp_tres_mins",       tres_grp.get("minutes")),
            ("grp_tres_run_mins",   tres_grp.get("active")),
        ):
            if entries := _norm_tres(raw):
                fields.append((attr, entries))

    return fields


def _account_attrs(acc: dict, assoc: Optional[dict], label_map: dict,
                   root_qos: list) -> list:
    """Build the (key, value) attribute list for a slurm_account resource block."""
    attrs = [(key, _hcl_val(val)) for key, val in _account_fields(acc, assoc, root_qos)]

    # depends_on — make child accounts wait for their parent to be created first.
    parent = (assoc or {}).get("parent_account") or ""
    if parent and parent not in SYSTEM_ACCOUNTS and parent in label_map:
        attrs.append((f"depends_on = [slurm_account.{label_map[parent]}]", None))

    return attrs


def generate_accounts(accounts: list, assocs: list) -> tuple:
    """Returns (hcl_content, import_blocks)."""
    # Account-level association lookup: account_name → assoc dict (user == "")
    acct_assoc: dict = {
        a["account"]: a
        for a in assocs
        if (a.get("user") == "" or a.get("user") is None) and a.get("account")
    }

    # Determine the root association's qos list — accounts that only have this
    # list set are inheriting it from root, not explicitly configuring it.
    root_assoc = acct_assoc.get("root") or {}
    root_qos   = sorted(x for x in (root_assoc.get("qos") or []) if x)

    # Pre-build the label map so _account_attrs can construct depends_on.
    # Use assoc parent_account (not the account object) for depth calculation.
    def acc_parent(name: str) -> str:
        return (acct_assoc.get(name) or {}).get("parent_account") or ""

    def depth(name: str) -> int:
        d, p, seen = 0, acc_parent(name), set()
        while p and p not in SYSTEM_ACCOUNTS and p not in seen:
            seen.add(p); p = acc_parent(p); d += 1
        return d

    label_map: dict = {
        a["name"]: tf_label(a["name"], "account")
        for a in accounts
        if a["name"] not in SYSTEM_ACCOUNTS
    }

    blocks  = []
    imports = []

    for acc in sorted(accounts, key=lambda a: (depth(a["name"]), a["name"])):
        name = acc["name"]
        if name in SYSTEM_ACCOUNTS:
            continue
        label = label_map[name]
        attrs = _account_attrs(acc, acct_assoc.get(name), label_map, root_qos)
        blocks.append(resource_block("slurm_account", label, attrs))
        imports.append(import_block("slurm_account", label, name))

    return "\n\n".join(blocks), imports


