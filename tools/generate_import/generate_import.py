#!/usr/bin/env python3
"""
generate_import.py — Generate OpenTofu HCL + import blocks from an existing Slurm cluster.

Queries slurmrestd and writes four HCL files plus an imports file into the
output directory.  After running the script the workflow is:

  cd <output-dir>
  tofu init
  tofu apply        # import blocks pull Slurm state into OpenTofu state
  tofu apply        # reconcile: writes config-declared values back to Slurm
  tofu plan         # must be clean (no changes)

See the README in the same directory for full usage details.
"""

import argparse
import json
import os
import re
import sys
import urllib.error
import urllib.request
from collections import defaultdict
from typing import Optional


# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

# Built-in Slurm QOS that must not be destroyed via the REST API.
SYSTEM_QOS = {"normal"}

# Built-in accounts / users that are always present and should not be managed.
SYSTEM_ACCOUNTS = {"root"}
SYSTEM_USERS = {"root"}

HEADER = """\
# Generated by tools/generate_import/generate_import.py
# Do not edit by hand — re-run the script to regenerate.
#
# After 'tofu init':
#   tofu apply        # imports Slurm state into OpenTofu state
#   tofu apply        # reconcile: writes config values back to Slurm
#   tofu plan         # must show no changes
"""


# ---------------------------------------------------------------------------
# Slurm API client  (stdlib only — no third-party dependencies)
# ---------------------------------------------------------------------------

class SlurmClient:
    def __init__(self, endpoint: str, token: str, cluster: str, api_version: str):
        self.cluster = cluster
        self._base = f"{endpoint.rstrip('/')}/slurmdb/{api_version}"
        self._headers = {
            "X-SLURM-USER-TOKEN": token,
            "Content-Type": "application/json",
            "Accept": "application/json",
        }

    def _get(self, path: str, params: Optional[dict] = None) -> dict:
        url = f"{self._base}/{path.lstrip('/')}"
        if params:
            qs = "&".join(f"{k}={v}" for k, v in params.items())
            url = f"{url}?{qs}"
        req = urllib.request.Request(url, headers=self._headers)
        try:
            with urllib.request.urlopen(req, timeout=30) as resp:
                return json.loads(resp.read())
        except urllib.error.HTTPError as exc:
            body = exc.read().decode(errors="replace")
            raise SystemExit(f"Slurm API error {exc.code} on GET {url}:\n{body}") from exc
        except urllib.error.URLError as exc:
            raise SystemExit(f"Cannot reach slurmrestd at {url}: {exc.reason}") from exc

    def qos(self) -> list:
        return self._get("qos/").get("qos", [])

    def accounts(self) -> list:
        return self._get("accounts/").get("accounts", [])

    def associations(self) -> list:
        # Scope to the configured cluster so we don't pick up stale multi-cluster rows.
        return self._get("associations/", {"cluster": self.cluster}).get("associations", [])

    def users(self) -> list:
        return self._get("users/").get("users", [])


# ---------------------------------------------------------------------------
# Name sanitization
# ---------------------------------------------------------------------------

_name_registry: dict = {}   # rtype -> {tf_label: slurm_name}


def tf_label(slurm_name: str, rtype: str) -> str:
    """
    Convert a Slurm name to a valid Terraform resource label.

    Rules:
    - Replace any character outside [a-zA-Z0-9_] with _
    - If the result starts with a digit, prepend {rtype}_
    - Abort with a clear error on label collision.
    """
    label = re.sub(r"[^a-zA-Z0-9_]", "_", slurm_name)
    if label and label[0].isdigit():
        label = f"{rtype}_{label}"

    registry = _name_registry.setdefault(rtype, {})
    if label in registry and registry[label] != slurm_name:
        raise SystemExit(
            f"Name collision in {rtype}: Slurm names '{slurm_name}' and "
            f"'{registry[label]}' both map to Terraform label '{label}'.\n"
            f"Rename one of them in Slurm before running this script."
        )
    registry[label] = slurm_name
    return label


# ---------------------------------------------------------------------------
# HCL rendering helpers
# ---------------------------------------------------------------------------

def _q(s: str) -> str:
    """Double-quote an HCL string value."""
    return '"' + s.replace("\\", "\\\\").replace('"', '\\"') + '"'


def _strlist(items: list) -> str:
    return "[" + ", ".join(_q(x) for x in items) + "]"


def _tres_list(tres: list) -> str:
    """Render a TRES list as an HCL list of inline objects."""
    parts = []
    for t in tres:
        fields = [f"type = {_q(t['type'])}"]
        if t.get("name"):
            fields.append(f"name = {_q(t['name'])}")
        fields.append(f"count = {t['count']}")
        parts.append("{ " + ", ".join(fields) + " }")
    return "[\n    " + ",\n    ".join(parts) + ",\n  ]"


def _hcl_val(val) -> str:
    """
    Render a native Python value as an HCL right-hand side. Used by both the
    flat renderers below and (indirectly) as the single source of truth for how
    a field maps to HCL. Recognised kinds: bool, int, float, str, list-of-str,
    list-of-dict (TRES).
    """
    if isinstance(val, bool):
        return "true" if val else "false"
    if isinstance(val, int):
        return str(val)
    if isinstance(val, float):
        return repr(val)
    if isinstance(val, str):
        return _q(val)
    if isinstance(val, list):
        if val and isinstance(val[0], dict):
            return _tres_list(val)
        return _strlist(val)
    raise TypeError(f"cannot render HCL value of type {type(val).__name__}: {val!r}")


def resource_block(rtype: str, label: str, attrs: list, comment: str = "") -> str:
    """
    Render a Terraform resource block.

    Each entry in attrs is a (key, value) tuple:
    - (key, str)  → emitted as  key = value
    - (text, None) → emitted verbatim (indented), used for nested blocks and
                     meta-arguments like depends_on.
    """
    lines = []
    if comment:
        lines.append(comment)
    lines.append(f'resource "{rtype}" "{label}" {{')
    for key, val in attrs:
        if val is None:
            for line in key.splitlines():
                lines.append(f"  {line}" if line.strip() else "")
        else:
            lines.append(f"  {key} = {val}")
    lines.append("}")
    return "\n".join(lines)


def import_block(rtype: str, label: str, slurm_id: str) -> str:
    return f'import {{\n  to = {rtype}.{label}\n  id = {_q(slurm_id)}\n}}'


# ---------------------------------------------------------------------------
# SlurmInt / SlurmFloat value extraction
# ---------------------------------------------------------------------------

def sint(obj) -> Optional[int]:
    """
    Extract a usable integer from a SlurmInt dict ({number, set, infinite}).
    Returns None for unset, infinite, or zero values (Slurm's "no limit").
    """
    if obj is None:
        return None
    if isinstance(obj, int):
        return obj if obj > 0 else None
    if isinstance(obj, dict):
        if obj.get("infinite") or not obj.get("set"):
            return None
        n = obj.get("number", 0)
        return n if n > 0 else None
    return None


# Slurm stores fairshare=parent as shares_raw = INT32_MAX (SLURMDB_FS_USE_PARENT)
# and the REST API surfaces it as exactly that number, with no accompanying
# flag -- verified identical across Slurm 25.05 / 25.11 / 26.05. The provider's
# slurm_account / slurm_user fairshare attribute is a STRING for this reason: an
# integer weight, or the literal "parent".
FAIRSHARE_PARENT = 2147483647


def _fairshare_emit(shares) -> Optional[str]:
    """
    Map a raw shares_raw value to the string the provider's fairshare attribute
    expects, or None to skip emitting it:

      - INT32_MAX (SLURMDB_FS_USE_PARENT) -> "parent"
      - any weight > 1                    -> its decimal string
      - 0, 1, or non-int                  -> None (skip)

    Skipping <= 1 preserves existing behaviour: 1 is Slurm's default and
    indistinguishable from an explicit 1; 0 means "inherit from parent" and is
    likewise Slurm's own fallback, not declared intent.
    """
    if not isinstance(shares, int):
        return None
    if shares == FAIRSHARE_PARENT:
        return "parent"
    if shares > 1:
        return str(shares)
    return None


def sfloat(obj) -> Optional[float]:
    """Extract a float from a SlurmFloat dict. Returns None if unset."""
    if obj is None:
        return None
    if isinstance(obj, (int, float)):
        return float(obj)
    if isinstance(obj, dict) and obj.get("set"):
        return float(obj.get("number", 0))
    return None


# ---------------------------------------------------------------------------
# TRES normalisation
# ---------------------------------------------------------------------------

def _norm_tres(raw) -> list:
    """
    Normalise a TRES list from the Slurm API into [{type, name?, count}].
    Skips entries with zero or unset counts.
    """
    if not raw:
        return []
    out = []
    for t in raw:
        if not isinstance(t, dict):
            continue
        raw_count = t.get("count", 0)
        count = sint(raw_count) if isinstance(raw_count, dict) else (int(raw_count) if raw_count else 0)
        if not count:
            continue
        entry: dict = {"type": t["type"], "count": count}
        if t.get("name"):
            entry["name"] = t["name"]
        out.append(entry)
    return out


