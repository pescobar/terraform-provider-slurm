def generate_bigcluster_generate_tf(qos_labels: list) -> str:
    """Fill the generate.tf template, injecting the QOS depends_on for accounts."""
    if qos_labels:
        refs = ", ".join(f"slurm_qos.{lbl}" for lbl in qos_labels)
        dep = ("\n\n  # QOS referenced by name must exist before the accounts.\n"
               f"  depends_on = [{refs}]")
    else:
        dep = ""
    return BIGCLUSTER_GENERATE_TF.replace("{account_depends_on}", dep)


# ---------------------------------------------------------------------------
# provider.tf
# ---------------------------------------------------------------------------

def generate_provider(endpoint: str, cluster: str, api_version: str) -> str:
    return f"""\
terraform {{
  required_providers {{
    slurm = {{
      source  = "pescobar/slurm"
      version = "~> 0.1"
    }}
  }}
}}

variable "slurm_token" {{
  type      = string
  sensitive = true
}}

variable "slurm_api_version" {{
  type    = string
  default = {_q(api_version)}
}}

provider "slurm" {{
  endpoint    = {_q(endpoint)}
  token       = var.slurm_token
  cluster     = {_q(cluster)}
  api_version = var.slurm_api_version
}}
"""


# ---------------------------------------------------------------------------
# File writing
# ---------------------------------------------------------------------------

def write(path: str, content: str) -> None:
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"  wrote {path}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Generate OpenTofu HCL + import blocks from a running Slurm cluster.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    p.add_argument(
        "--endpoint",
        default=os.environ.get("SLURM_REST_URL", "http://localhost:6820"),
        help="slurmrestd base URL (env: SLURM_REST_URL)",
    )
    p.add_argument(
        "--token",
        default=os.environ.get("SLURM_JWT_TOKEN", ""),
        help="JWT token (env: SLURM_JWT_TOKEN)",
    )
    p.add_argument(
        "--cluster",
        default=os.environ.get("SLURM_CLUSTER", "linux"),
        help="Slurm cluster name (env: SLURM_CLUSTER)",
    )
    p.add_argument(
        "--api-version",
        default=os.environ.get("SLURM_API_VERSION", "v0.0.42"),
        help="slurmrestd API version (env: SLURM_API_VERSION)",
    )
    p.add_argument(
        "--output-dir",
        default="./generated",
        help="Directory to write the generated .tf files into",
    )
    p.add_argument(
        "--layout",
        choices=("flat", "big-cluster"),
        default="flat",
        help="Output layout: 'flat' (one HCL block per resource) or "
             "'big-cluster' (account-centric YAML data + for_each generation, "
             "recommended for large clusters)",
    )
    return p.parse_args()


def main() -> None:
    args = parse_args()

    if not args.token:
        raise SystemExit(
            "No JWT token provided. Pass --token or set SLURM_JWT_TOKEN."
        )

    out = args.output_dir
    os.makedirs(out, exist_ok=True)

    client = SlurmClient(args.endpoint, args.token, args.cluster, args.api_version)

    print("Querying Slurm API...")
    print(f"  endpoint    : {args.endpoint}")
    print(f"  cluster     : {args.cluster}")
    print(f"  api-version : {args.api_version}")

    qos_list = client.qos()
    accounts = client.accounts()
    assocs   = client.associations()
    users    = client.users()

    print(f"  found {len(qos_list)} QOS, {len(accounts)} accounts, "
          f"{len(users)} users, {len(assocs)} associations")
    print()

    all_imports: list = []
    all_warnings: list = []

    # QOS — identical HCL in both layouts.
    qos_hcl, qos_imports, qos_warnings = generate_qos(qos_list)
    all_imports.extend(qos_imports)
    all_warnings.extend(qos_warnings)

    if args.layout == "big-cluster":
        # QOS labels, recomputed for the accounts' depends_on (tf_label is
        # idempotent for an already-seen name).
        qos_labels = [tf_label(q["name"], "qos")
                      for q in sorted(qos_list, key=lambda x: x["name"])]

        account_files, acc_imports = generate_bigcluster_accounts(accounts, assocs)
        exceptions, usr_imports, skipped = generate_bigcluster_users(users, assocs)
        all_imports.extend(acc_imports)
        all_imports.extend(usr_imports)

        print(f"Generating {len(all_imports)} resources into {out}/ (big-cluster layout)")
        write(os.path.join(out, "main.tf"),
              generate_provider(args.endpoint, args.cluster, args.api_version))
        write(os.path.join(out, "qos.tf"), HEADER + "\n" + qos_hcl + "\n")
        write(os.path.join(out, "generate.tf"),
              generate_bigcluster_generate_tf(qos_labels))
        os.makedirs(os.path.join(out, "data", "accounts"), exist_ok=True)
        for relpath, content in account_files:
            write(os.path.join(out, relpath), content)
        os.makedirs(os.path.join(out, "data", "users"), exist_ok=True)
        for relpath, content in _render_users_yaml_files(exceptions):
            write(os.path.join(out, relpath), content)
        write(os.path.join(out, "imports.tf"),
              HEADER + "\n" + "\n\n".join(all_imports) + "\n")

        if skipped:
            all_warnings.append(
                f"  {len(skipped)} user(s) have no associations and cannot be\n"
                f"  represented in the account-centric layout — skipped:\n"
                f"    {', '.join(skipped[:20])}"
                + (" …" if len(skipped) > 20 else "")
            )
    else:
        # Accounts
        acc_hcl, acc_imports = generate_accounts(accounts, assocs)
        all_imports.extend(acc_imports)

        # Users
        usr_hcl, usr_imports, skipped = generate_users(users, assocs)
        all_imports.extend(usr_imports)

        print(f"Generating {len(all_imports)} resources into {out}/")
        write(os.path.join(out, "provider.tf"),
              generate_provider(args.endpoint, args.cluster, args.api_version))
        write(os.path.join(out, "qos.tf"),
              HEADER + "\n" + qos_hcl + "\n")
        write(os.path.join(out, "accounts.tf"),
              HEADER + "\n" + acc_hcl + "\n")
        write(os.path.join(out, "users.tf"),
              HEADER + "\n" + usr_hcl + "\n")
        write(os.path.join(out, "imports.tf"),
              HEADER + "\n" + "\n\n".join(all_imports) + "\n")

        if skipped:
            all_warnings.append(
                f"  {len(skipped)} user(s) have no associations and cannot be\n"
                f"  represented as a slurm_user resource (default_account and at\n"
                f"  least one association are both required) — skipped:\n"
                f"    {', '.join(skipped[:20])}"
                + (" …" if len(skipped) > 20 else "")
            )

    if all_warnings:
        print()
        print("WARNINGS:")
        for w in all_warnings:
            print(w)

    print()
    print("Done.  Next steps:")
    print(f"  cd {out}")
    print("  tofu init")
    print("  tofu apply        # imports Slurm state into OpenTofu state")
    print("  tofu apply        # reconcile: writes config values back to Slurm")
    print("  tofu plan         # must show no changes")


if __name__ == "__main__":
    main()
